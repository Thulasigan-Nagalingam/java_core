class PrintDemo{
	public static void main(String args[]){
		System.out.println("My name is A");
		System.out.println("My father's name is B");
		System.out.println("My age is 20");
		System.out.println( );
		System.out.print("My name is A");
		System.out.print("My father's name is B");
		System.out.print("My age is 20");
		System.out.println( );
		System.out.print("My name is A\n");
		System.out.print("My father's name is B\n");
		System.out.print("My age is 20\n");
	}
}